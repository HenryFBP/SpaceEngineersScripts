            /// Here is an example of a script, using my dictionary,
            /// that lists all of the characters that are as wide
            /// as a pound symbol, or '#'.
            /// 

            Echo($"Hi! I'm main! I was passed: '{argument}'!");

            IMyTextPanel lcd = GridTerminalSystem?.GetBlockWithName(_LCD_NAME) as IMyTextPanel;

            char symbol = '#';

            List<char> symbols = new List<char> { symbol, };

            foreach(KeyValuePair<int, int[]> entry in fontData)
            {
                if (entry.Value[(int)fontDataE.SIZE_X] == fontData[symbol][(int)fontDataE.SIZE_X])
                {//if widths are the same

                    symbols.Add((char)entry.Key); //write down key, which is the char.
                }

            }

            Echo($"The {symbol} character is {fontData[symbol][(int)fontDataE.SIZE_X]} units wide. Below are similarly-wide characters:");

            String temp = "";

            for (int i = 0; i < symbols.Count; i++)
            {
                temp += $"'{symbols[i]}'";

                if(i < symbols.Count) //if not at end, add comma.
                {
                    temp += ", ";
                }
            }
            Echo(temp);

            Echo(@"Just to make sure I'm not lying, I'm going to " +
                @"output some lines of these characters to a " +
                @"text panel that you'll specify near the " +
                @"beginning of this program.");

            Echo("If the script works, the little '|' vertical bar characters should line up precisely," +
                "since they're going after the same-width character sequences!");
            

            for(int i = 0; i < symbols.Count; i++) //go through all symbols
            {
                temp = "";
                for(int j = 0; j < 20; j++)
                {
                    temp = temp + symbols[i]; //make 20-char line of characters
                }
                temp = temp + '|'; //add a bar.
                lcd?.WritePublicText(temp+'\n', true);

            }


        }
